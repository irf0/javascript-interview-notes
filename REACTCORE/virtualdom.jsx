//VIRTUAL DOM
//Virtual DOM is an in-memory UI representation used to to optimize updates

//The Idea:
//If one wall in our house gets dirty, we fix THAT wall only
//We do not break and rebuild the entire house again because that will be inefficient and expensive.

//VDOM = React firt plans the changes on paper, then fixes (repaints UI) ONLY whats's needed.

//Real World Usecases:
//1. Flipkart => ADD TO CART --> only increases the number of items not the whole page.
//2. Swiggy => RIDER ICON --> Only the rider icon moves in the order tracking not the whole screen rebuilds.

