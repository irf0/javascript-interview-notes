// DEBOUNCING
// Debouncing ensures that a function is only called after a specific delay has passed since the last time it was triggered.

//Examples:
// Search Bars -> Wait until the user stops typing before making an API call.
// Auto-save -> Save a draft only after the user pauses their work.

// THROTTLING
// Throttling ensures that a function is executed at most once in a specified time interval, regardless of how many times the event is triggered

//Examples:
// Scrolling -> Implementing infinite scroll or lazy loading while a user is actively scrolling.
// Rate-limiting -> Preventing a "Submit" button from being clicked dozens of times per second.